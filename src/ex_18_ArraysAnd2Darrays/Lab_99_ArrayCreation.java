package ex_18_ArraysAnd2Darrays;

public class Lab_99_ArrayCreation {
    public static void main(String[] args) {
        int[] nums = {10,15,20,9,70}; // This is integer array.
        System.out.println(nums.length); // gives the length of the array.
        String[] st = {"Hello","World","is","Beautiful"}; // string array is denoted by "".
        System.out.println(st.length);// gives the length of the string array
    }
}
