package ex_12_Functions;

public class Lab_072_FunctionWithoutReturnTypeAndWithoutParameter {
    public static void main(String[] args) {
        // this program is for creating function without Return type(void) and without parameter.

        getChocolate(); // getChocolate(); calls the function getChocolate() that has set of codes to perform.
    }
    public static void getChocolate() //This is the function that user creates that has block of codes to run while it is called.
    {
        System.out.println("Got the chocolate");
    }
}
